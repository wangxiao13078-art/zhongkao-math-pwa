// 辅助函数
export function getChapters(): string[] {
  const chapters = new Set<string>()
  questions.forEach(q => chapters.add(q.chapter))
  return Array.from(chapters)
}

export function getTypes(): string[] {
  const types = new Set<string>()
  questions.forEach(q => types.add(q.type))
  return Array.from(types)
}

export function getRandomQuestions(count: number): Question[] {
  const shuffled = [...questions].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

export function filterQuestions(filters: {
  type?: string
  chapter?: string
  difficulty?: number
}): Question[] {
  return questions.filter(q => {
    if (filters.type && q.type !== filters.type) return false
    if (filters.chapter && q.chapter !== filters.chapter) return false
    if (filters.difficulty && q.difficulty !== filters.difficulty) return false
    return true
  })
}

// 获取章节统计
export function getChapterStats(): { chapter: string; count: number }[] {
  const counts: Record<string, number> = {}
  questions.forEach(q => {
    counts[q.chapter] = (counts[q.chapter] || 0) + 1
  })
  return Object.entries(counts).map(([chapter, count]) => ({ chapter, count }))
}

// 获取难度统计
export function getDifficultyStats(): { difficulty: number; count: number }[] {
  const counts: Record<number, number> = {}
  questions.forEach(q => {
    counts[q.difficulty] = (counts[q.difficulty] || 0) + 1
  })
  return Object.entries(counts).map(([d, count]) => ({ difficulty: parseInt(d), count }))
}
